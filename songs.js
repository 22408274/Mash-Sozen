function highlightChords(text) {
  const chordRegex = /\b([A-G](?:#|b)?m?(?:7|maj7|sus2|sus4)?)\b/g;

  return text
    .split("\n")
    .map(line => {
      const isChordLine = /^[\sA-G#bm0-9maj7sus]+$/.test(line.trim());
      if (isChordLine) {
        return line.replace(chordRegex, '<span class="chord">$1</span>');
      }
      return line;
    })
    .join("\n");
}
